def update_locale(lang: str, mapping: dict):
    path = LOCALES_DIR / f"{lang}.json"
    with path.open("r", encoding="utf-8") as f:
        data = json.load(f)
    if "translation" not in data:
        print(f"[skip] {lang}: missing 'translation' root", file=sys.stderr)
        return
    section = data["translation"]
    written = 0
    missing_keys = []
    for k, v in mapping.items():
        if k not in section:
            missing_keys.append(k)
            continue
        if section[k] != v:
            section[k] = v
            written += 1
    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")
    print(f"[ok] {lang}: wrote {written} keys")
    if missing_keys:
        print(
            f"  warning: {len(missing_keys)} keys absent in locale file "
            f"(run `bun run i18n:extract` first): {missing_keys[:3]}...",
            file=sys.stderr,
        )


